#include <iostream>
using namespace std;
int main(){
    int i=1;
    int n;
    cout <<"Enter a Number : " << endl;
    cin >> n;
    while (i<=n){
        cout << i <<endl;
        i++; // i++ means i=i+1;
    }
    
}
